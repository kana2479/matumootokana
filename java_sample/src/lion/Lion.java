package lion;

public class Lion {

	/*---------フィールド----------*/
	public String animalName;
	public double bodyLength;
	public int speed;

	/*-----------getter/setter---------*/
	public String getAnimalame() {
		return animalName;
	}

	public void setAnimalame(String animalName) {
		this.animalName = animalName;
	}

	public double getBodyLength() {
		return bodyLength;
	}

	public void setBodyLength(double bodyLength) {
		this.bodyLength = bodyLength;
	}

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}
}
