package selfintroduction;

public class Superclass {
	/*------フィールド--------*/
	public String name;

	/*-------コンストラクタ------*/
	Superclass(String name) {
		this.name = name;
	}

	/*-------getter/setter---------*/
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
